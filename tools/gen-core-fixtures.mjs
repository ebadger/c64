// Generate committed core test fixtures from the src/ assembler:
//
//   node tools/gen-core-fixtures.mjs          verify committed fixtures match (fails on drift)
//   node tools/gen-core-fixtures.mjs --write  regenerate the committed fixtures
//
// Emits a C++ header consumed by the native golden test and a JSON file consumed by the headless
// WASM smoke test, so both exercise identical assembler output against the same production core.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

import { buildBorderBgFixture } from "./core-fixture.mjs";

const here = dirname(fileURLToPath(import.meta.url));
const fixturesDir = join(here, "..", "core", "tests", "fixtures");

function jsonText(fixture) {
  return JSON.stringify(fixture, null, 2) + "\n";
}

function headerText(fixture) {
  const bytes = fixture.prg.map((b) => `0x${b.toString(16).padStart(2, "0")}`);
  const lines = [];
  for (let i = 0; i < bytes.length; i += 12) {
    lines.push("    " + bytes.slice(i, i + 12).join(", ") + ",");
  }
  const e = fixture.expected;
  return [
    "// GENERATED by tools/gen-core-fixtures.mjs from the src/ assembler. Do not edit by hand;",
    "// run `node tools/gen-core-fixtures.mjs --write` to regenerate. See tools/core-fixture.mjs.",
    "#ifndef C64_FIXTURE_BORDER_BG_HPP",
    "#define C64_FIXTURE_BORDER_BG_HPP",
    "",
    "#include <cstdint>",
    "",
    "namespace c64_fixture {",
    "",
    "// Direct-mode PRG: 2-byte load address followed by code that sets $D020/$D021 then spins.",
    "inline constexpr std::uint8_t kBorderBgPrg[] = {",
    ...lines,
    "};",
    "",
    `inline constexpr std::uint16_t kBorderBgLoadAddress = 0x${fixture.loadAddress.toString(16)};`,
    `inline constexpr std::uint16_t kBorderBgRunAddress = 0x${fixture.runAddress.toString(16)};`,
    `inline constexpr std::uint8_t kExpectedBorder = 0x${e.border.toString(16).padStart(2, "0")};`,
    `inline constexpr std::uint8_t kExpectedBackground = 0x${e.background.toString(16).padStart(2, "0")};`,
    `inline constexpr std::uint16_t kFrameWidth = ${e.width};`,
    `inline constexpr std::uint16_t kFrameHeight = ${e.height};`,
    `inline constexpr int kBorderSampleIndex = ${e.borderSample.index};`,
    `inline constexpr int kCentreSampleIndex = ${e.centreSample.index};`,
    "",
    "} // namespace c64_fixture",
    "",
    "#endif // C64_FIXTURE_BORDER_BG_HPP",
    "",
  ].join("\n");
}

const write = process.argv.includes("--write");
const fixture = buildBorderBgFixture();
const jsonPath = join(fixturesDir, "border_bg_fixture.json");
const headerPath = join(fixturesDir, "border_bg_fixture.hpp");
const json = jsonText(fixture);
const header = headerText(fixture);

if (write) {
  writeFileSync(jsonPath, json);
  writeFileSync(headerPath, header);
  console.log(`wrote ${jsonPath}`);
  console.log(`wrote ${headerPath}`);
  process.exit(0);
}

let failures = 0;
for (const [path, expected] of [
  [jsonPath, json],
  [headerPath, header],
]) {
  let actual = null;
  try {
    actual = readFileSync(path, "utf8");
  } catch {
    console.error(`MISSING ${path} (run --write)`);
    failures += 1;
    continue;
  }
  if (actual !== expected) {
    console.error(`DRIFT ${path} (run --write to regenerate)`);
    failures += 1;
  } else {
    console.log(`OK ${path}`);
  }
}
process.exit(failures === 0 ? 0 : 1);
