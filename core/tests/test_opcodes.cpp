TEST(opcodes_documented_set_is_exactly_151) {
  std::set<u8> documented(std::begin(kDocumented), std::end(kDocumented));
  CHECK_EQ(documented.size(), 151u);  // no duplicates in the reference list

  int count = 0;
  for (int op = 0; op <= 255; ++op) {
    const CpuOpcodeInfo info = cpuOpcodeInfo(static_cast<u8>(op));
    const bool expected = documented.count(static_cast<u8>(op)) != 0;
    CHECK_EQ(info.documented, expected);
    if (info.documented) ++count;
  }
  CHECK_EQ(count, 151);
}

TEST(opcodes_page_cross_flags) {
  std::set<u8> cross(std::begin(kPageCross), std::end(kPageCross));
  CHECK_EQ(cross.size(), 23u);
  for (int op = 0; op <= 255; ++op) {
    const CpuOpcodeInfo info = cpuOpcodeInfo(static_cast<u8>(op));
    const bool expected = cross.count(static_cast<u8>(op)) != 0;
    CHECK_EQ(info.pageCrossPenalty, expected);
  }
}

TEST(opcodes_cycle_counts_by_family) {
  auto cyc = [](u8 op) { return cpuOpcodeInfo(op).baseCycles; };
  // Immediate = 2, zp = 3, zp,X = 4, abs = 4, abs,X/Y read = 4, (zp,X) = 6, (zp),Y read = 5.
  CHECK_EQ(cyc(0xA9), 2u);
  CHECK_EQ(cyc(0xA5), 3u);
  CHECK_EQ(cyc(0xB5), 4u);
  CHECK_EQ(cyc(0xAD), 4u);
  CHECK_EQ(cyc(0xBD), 4u);
  CHECK_EQ(cyc(0xA1), 6u);
  CHECK_EQ(cyc(0xB1), 5u);
  // Stores that never take a page-cross penalty carry the fixed higher count.
  CHECK_EQ(cyc(0x9D), 5u);  // STA abs,X
  CHECK_EQ(cyc(0x91), 6u);  // STA (zp),Y
  // RMW.
  CHECK_EQ(cyc(0xE6), 5u);  // INC zp
  CHECK_EQ(cyc(0xF6), 6u);  // INC zp,X
  CHECK_EQ(cyc(0xEE), 6u);  // INC abs
  CHECK_EQ(cyc(0xFE), 7u);  // INC abs,X
  CHECK_EQ(cyc(0x0A), 2u);  // ASL A
  // Control / stack / system.
  CHECK_EQ(cyc(0x4C), 3u);  // JMP abs
  CHECK_EQ(cyc(0x6C), 5u);  // JMP (ind)
  CHECK_EQ(cyc(0x20), 6u);  // JSR
  CHECK_EQ(cyc(0x60), 6u);  // RTS
  CHECK_EQ(cyc(0x40), 6u);  // RTI
  CHECK_EQ(cyc(0x48), 3u);  // PHA
  CHECK_EQ(cyc(0x68), 4u);  // PLA
  CHECK_EQ(cyc(0x00), 7u);  // BRK
  // All branches and all implied register/flag ops are 2 cycles.
  const u8 branches[] = {0x10, 0x30, 0x50, 0x70, 0x90, 0xB0, 0xD0, 0xF0};
  for (u8 b : branches) CHECK_EQ(cyc(b), 2u);
  const u8 implied[] = {0xAA, 0xA8, 0x8A, 0x98, 0xBA, 0x9A, 0xE8, 0xC8, 0xCA, 0x88,
                        0x18, 0x38, 0x58, 0x78, 0xB8, 0xD8, 0xF8, 0xEA};
  for (u8 i : implied) CHECK_EQ(cyc(i), 2u);
}
